package ex2.exsecise1;

public class Student {
    private String name;
    private int year;
    
    public Student(String name, int year) {
        this.name = name;
        this.year = year;
    }

    //get set
    public String getName() {
        return name;
    }

    public int getYear() {
        return year;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setYear(int year) {
        this.year = year;
    }

    
    
}
